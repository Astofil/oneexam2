public class Circle
{
    double pi;
    double radius;
    public Circle()
    {
        radius=new radius(pi);
    }
    public double GetArea()
    {
        return (pi*radius*radius);
    }
    public double GetDiameter()
    {
        return (radius*2);
    }
    public double GetCircumference()
    {
        return (2*pi*radius);
    }
    public double GetRadius()
    {
        return radius;
    }
}